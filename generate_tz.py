from docx.oxml.ns import qn


def set_cell_border(cell, **kwargs):
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    tcBorders = tcPr.find(qn('w:tcBorders'))
    if tcBorders is None:
        tcBorders = tc.get_or_add_tcPr().makeelement(qn('w:tcBorders'), {})
        tcPr.append(tcBorders)
    for edge, val in kwargs.items():
        element = tcBorders.find(qn(f'w:{edge}'))
        if element is None:
            element = tc.get_or_add_tcPr().makeelement(qn(f'w:{edge}'), {})
            tcBorders.append(element)
        element.set(qn('w:val'), val.get('val', 'single'))
        element.set(qn('w:sz'), val.get('sz', '4'))
        element.set(qn('w:color'), val.get('color', '000000'))
        element.set(qn('w:space'), val.get('space', '0'))


def apply_borders(cell):
    b = {'val': 'single', 'sz': '4', 'color': '000000', 'space': '0'}
    set_cell_border(cell, top=b, bottom=b, start=b, end=b)


def add_run(paragraph, text, bold=False, size=11, font='Calibri'):
    run = paragraph.add_run(text)
    run.bold = bold
    run.font.size = Pt(size)
    run.font.name = font
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = run._element.get_or_add_rPr().makeelement(qn('w:rFonts'), {})
        rPr.append(rFonts)
    rFonts.set(qn('w:eastAsia'), font)
    rFonts.set(qn('w:cs'), font)
    return run


def add_section_heading(doc, text, size=11):
    p = doc.add_paragraph()
    add_run(p, text, bold=True, size=size)
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(6)
    return p


def build_cell_content(cell, lines):
    """lines: list of (text, bold, size) tuples."""
    cell.text = ''
    for i, (text, bold, sz) in enumerate(lines):
        if i == 0:
            p = cell.paragraphs[0]
        else:
            p = cell.add_paragraph()
        p.paragraph_format.space_after = Pt(2)
        add_run(p, text, bold=bold, size=sz)


def add_single_cell_table(doc, lines_list):
    """Create a table with N rows x 1 col. lines_list: list of list of (text, bold, size)."""
    table = doc.add_table(rows=len(lines_list), cols=1)
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    tbl = table._tbl
    tblPr = tbl.tblPr if tbl.tblPr is not None else tbl.get_or_add_tblPr()
    tblW = tblPr.find(qn('w:tblW'))
    if tblW is None:
        tblW = tblPr.makeelement(qn('w:tblW'), {})
        tblPr.append(tblW)
    tblW.set(qn('w:w'), '9638')
    tblW.set(qn('w:type'), 'dxa')

    for i, lines in enumerate(lines_list):
        cell = table.cell(i, 0)
        build_cell_content(cell, lines)
        apply_borders(cell)
    return table


def set_default_font(doc, name='Calibri', size=11):
    style = doc.styles['Normal']
    font = style.font
    font.name = name
    font.size = Pt(size)
    rPr = style.element.find(qn('w:rPr'))
    if rPr is None:
        rPr = style.element.makeelement(qn('w:rPr'), {})
        style.element.append(rPr)
    rFonts = rPr.find(qn('w:rFonts'))
    if rFonts is None:
        rFonts = rPr.makeelement(qn('w:rFonts'), {})
        rPr.append(rFonts)
    rFonts.set(qn('w:eastAsia'), name)
    rFonts.set(qn('w:cs'), name)


